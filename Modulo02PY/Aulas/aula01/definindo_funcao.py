# A palavra 'def' DEFINE (cria) uma funcao.
# O codigo indentado abaixo so roda quando a funcao e CHAMADA.
def saudacao():
    print("Ola! Bem-vindo a Python Academy.")

# Chamando (executando) a funcao pelo nome + parenteses.
# Podemos chamar quantas vezes quisermos:
saudacao()
saudacao()
